    private void testData(JTable table) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        CSVDataReader csvDataReader = new CSVDataReader();
        ArrayList<CSVData> csvData = csvDataReader.retrieveCSVData();

        Iterator iterator = csvData.iterator();

        while (iterator.hasNext()){
            CSVData next = (CSVData) iterator.next();
            model.addRow(new Object[]{next.getId(),next.getName(), next.getCategory(),next.getAmount(),next.getQuantity()});
        }

    }

